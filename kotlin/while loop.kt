fun main(){
    var i=1
    while(i<=5){
        print("$i ")
        i++
    }
    println()
    i=1
    while(i<5){
        print("$i ")
        i++
    }
    println()
    i=5
    while(i>=1){
        print("$i ")
        i--
    }
    println()
    i=1
    while(i<=5){
        print("$i ")
        i+=2
    }
    println()
    i=5
    while(i>=1){
        print("$i ")
        i-=2
    }
    println()
}